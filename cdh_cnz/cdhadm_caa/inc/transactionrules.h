/*===================================================================*/
/**
      @brief     TransactionRules

                 The services provides transaction rules.
 */
/*=================================================================== */

class TransactionRules
{
	/*=====================================================================
		                        PUBLIC DECLARATION SECTION
	==================================================================== */
public:
	/*=====================================================================
		                           PUBLIC ATTRIBUTES
	==================================================================== */
		/*=====================================================================
		                        ENUMERATED DECLARATION SECTION
		==================================================================== */
		/*=================================================================== */
		/**
		   @brief  Rules

		           -
		 */
		/*=================================================================== */
    enum Rules { TR_TRANSACTIONBEGIN         = 1,
                 TR_TRANSACTIONEND           = 2,
                 TR_TRANSACTIONCOMMIT        = 4,
                 TR_TRANSACTIONTERMINATE     = 8,
                 TR_GETLASTCOMMITTEDBLOCKNO  = 16
    };

    /*=====================================================================
    		                        PRIVATE DECLARATION SECTION
    ==================================================================== */

private:

    /*=====================================================================
    		                           PRIVATE ATTRIBUTES
    ==================================================================== */
	/*=================================================================== */
	/**
			      @brief   next_
						   -
	 */
	/*=================================================================== */
    Rules next_;
	/*=================================================================== */
	/**
			      @brief   prev_
						   -
	 */
	/*=================================================================== */
    Rules prev_;
    /*=====================================================================
    		                        PUBLIC DECLARATION SECTION
    ==================================================================== */
public:
	/*=====================================================================
	     	                        CLASS CONSTRUCTOR
	==================================================================== */
	/*=================================================================== */
	/**

	   	      @brief          TransactionRules
	   						  TransactionRules constructor.

	   		  @return          void

	   	      @exception       none
	 */
	/*=================================================================== */
    TransactionRules();
    
	/*=====================================================================
	     	                        CLASS DESTRUCTOR
	==================================================================== */
	/*=================================================================== */
	/**

	   	      @brief          ~TransactionRules
	   						  TransactionRules destructor.

	   		  @return          void

	   	      @exception       none
	 */
	/*=================================================================== */
    virtual ~TransactionRules();

	/*=================================================================== */
	/**

					    	      @brief          setNext
					    						  -

					    	      @return         void

					    	      @exception       throw()
	 */
	/*=================================================================== */
    void setNext();
	/*=================================================================== */
	/**

					    	      @brief          checkNext
					    						  -

					    		  @param          next
												  -

					    	      @return         true		on success
												  false     on unsuccessful

					    	      @exception       none

		 */
	/*=================================================================== */
    bool checkNext(Rules next);
	/*=================================================================== */
	/**

					    	      @brief          blockSent
					    						  -

					    	      @return         true		on success
												  false     on unsuccessful

					    	      @exception       none
	 */
	/*=================================================================== */
    bool blockSent();
};

